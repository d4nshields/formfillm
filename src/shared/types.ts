// ---------------------------------------------------------------------------
// Sensitivity — how careful we must be with a disclosure.
// ---------------------------------------------------------------------------

export const SENSITIVITIES = ["low", "medium", "high", "secret", "unknown"] as const;
export type Sensitivity = (typeof SENSITIVITIES)[number];

// ---------------------------------------------------------------------------
// Recommended action — what the assistant proposes for a field.
// ---------------------------------------------------------------------------

export const RECOMMENDED_ACTIONS = [
  "approve_candidate", // low sensitivity, safe to batch-approve after display
  "ask", // medium sensitivity, ask per field
  "ask_explicit", // high sensitivity, require explicit per-field confirmation
  "skip", // not relevant / no profile data
  "never_fill", // secrets — formfillm must never fill these
  "manual_review", // unclear, user must decide manually
] as const;
export type RecommendedAction = (typeof RECOMMENDED_ACTIONS)[number];

// ---------------------------------------------------------------------------
// Field metadata — produced by the scanner. CONTAINS NO PERSONAL VALUES.
// `hasValue` records only whether the field is currently non-empty, never the
// value itself. This object is what gets sent to the local LLM.
// ---------------------------------------------------------------------------

export const FIELD_KINDS = [
  "text",
  "textarea",
  "select",
  "checkbox",
  "radio",
  "aria-radio",
  "aria-listbox",
] as const;
export type FieldKind = (typeof FIELD_KINDS)[number];

export interface FieldOption {
  /** Option label as shown to the user. */
  label: string;
  /** Underlying value for native <option>; omitted for ARIA widgets. */
  value?: string;
}

export interface FieldRect {
  x: number;
  y: number;
  width: number;
  height: number;
}

export interface FieldMetadata {
  /** Stable id generated by the content script for this scan session. */
  fieldId: string;
  kind: FieldKind;
  tagName: string;
  inputType: string | null;
  name: string | null;
  domId: string | null;
  autocomplete: string | null;
  placeholder: string | null;
  ariaLabel: string | null;
  ariaLabelledByText: string | null;
  labelText: string | null;
  nearbyText: string | null;
  sectionHeading: string | null;
  required: boolean;
  disabled: boolean;
  readonly: boolean;
  /** Whether the field already holds a value. NEVER the value itself. */
  hasValue: boolean;
  /** Present for select / radio / checkbox-group / ARIA widgets. */
  options?: FieldOption[];
  rect: FieldRect;
  visible: boolean;
  /** Narrow form context — id/name only, never field values. */
  formContext: string | null;
}

// ---------------------------------------------------------------------------
// Classification — produced by the local LLM, validated before use.
// ---------------------------------------------------------------------------

export interface FieldClassification {
  fieldId: string;
  category: FieldCategory;
  sensitivity: Sensitivity;
  /** 0..1 */
  confidence: number;
  plainLanguageReason: string;
  possiblePurpose: string;
  recommendedAction: RecommendedAction;
  profileKeySuggestion: string | null;
  warnings: string[];
}

