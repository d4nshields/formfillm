// ---------------------------------------------------------------------------
// Disclosure ledger — records categories and decisions, NEVER actual values.
// ---------------------------------------------------------------------------

export const LEDGER_DECISIONS = [
  "approved",
  "edited",
  "skipped",
  "marked_wrong",
  "never_fill",
] as const;
export type LedgerDecision = (typeof LEDGER_DECISIONS)[number];

export interface LedgerEntry {
  /** Epoch milliseconds. */
  timestamp: number;
  siteOrigin: string;
  pageTitle: string | null;
  fieldLabel: string;
  category: FieldCategory;
  sensitivity: Sensitivity;
  decision: LedgerDecision;
  /** Always false in this MVP — proof we do not persist values. */
  valueStored: false;
  filled: boolean;
}

// ---------------------------------------------------------------------------
// Settings — Ollama config and local-only enforcement.
// ---------------------------------------------------------------------------

export interface Settings {
  ollamaBaseUrl: string;
  model: string;
  temperature: number;
  /** Locked on for the MVP — surfaced in UI but not user-disablable. */
  localOnly: true;
  /** Use Ollama structured/JSON output mode when supported. */
  jsonSchemaMode: boolean;
}

