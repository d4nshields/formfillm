/*
 * formfillm — shared type definitions
 *
 * These types are the contract between the content script, the background
 * service worker, and the side panel. Keeping them in one dependency-free
 * module means the privacy-critical logic (sensitivity, redaction, policy)
 * can be unit-tested without any Chrome APIs.
 */

